//Relational Operators
public class Program2 {
     public static void main(String[] args) {
        int x = 15, y = 10;
        System.out.println("x > y : " + (x > y));
        System.out.println("x < y : " + (x < y));
        System.out.println("x == y : " + (x == y));
        System.out.println("x != y : " + (x != y));
    }
}
