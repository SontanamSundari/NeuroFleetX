package OperatorPrograms;
//Logical Operators
public class Program3 {
    public static void main(String[] args) {
        int a = 5, b = 10, c = 15;
        System.out.println((a < b) && (b > c));  
        System.out.println((a > b) || (b < c));  
        System.out.println(!(a > b));            
    }
}
